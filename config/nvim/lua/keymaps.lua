local map = vim.keymap.set
map("n", "<A-s>", "<cmd>w<cr>", { desc = "Save" })
map("n", "<A-S>", "<cmd>wa<cr>", { desc = "Save all" })
map("n", "<A-h>", "<C-W>h", { desc = "Left window" })
map("n", "<A-l>", "<C-W>l", { desc = "Right window" })
map("n", "<A-j>", "<C-W>j", { desc = "Window below" })
map("n", "<A-k>", "<C-W>k", { desc = "Window above" })
map("n", "<A-q>", "<cmd>q!<cr>", { desc = "Quit without saving" })
map("n", "<A-Q>", "<cmd>qa!<cr>", { desc = "Quit all without saving" })
map("n", "<A-w>", "<cmd>bdelete<cr>", { desc = "Close current buffer" })
map("n", "<C-w><C-h>", "<cmd>wincmd H<cr>", { desc = "Move win to left" })
map("n", "<C-w><C-j>", "<cmd>wincmd J<cr>", { desc = "Move win to below" })
map("n", "<C-w><C-k>", "<cmd>wincmd K<cr>", { desc = "Move win to above" })
map("n", "<C-w><C-l>", "<cmd>wincmd L<cr>", { desc = "Move win to right" })
map("n", "<esc>", "<cmd>nohlsearch<cr>", { desc = "Clear highlight" })
map("n", "<localleader>l", "<cmd>Lazy<cr>", { desc = "Lazy panel" })
